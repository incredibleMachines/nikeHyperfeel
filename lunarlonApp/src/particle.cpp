//------------------------------------------------------------
void particle::addDampingForce(float _damping){
	
	// the usual way to write this is  vel *= 0.99
	// basically, subtract some part of the velocity
	// damping is a force operating in the oposite direction of the
	// velocity vector
	
    frc.x = frc.x - vel.x * _damping;
    frc.y = frc.y - vel.y * _damping;
}

//------------------------------------------------------------
void particle::setInitialCondition(float px, float py, float vx, float vy, float _damping){
    damping = _damping; // copy to global variable
    pos.set(px,py);
	vel.set(vx,vy);
    
    particleRadius = 1.5;
}

//------------------------------------------------------------
void particle::update(){
	
	if (bFixed == false){
		vel = vel + frc;
		pos = pos + vel;
	}
}

//------------------------------------------------------------
void particle::draw(){
    ofPushStyle();
    ofSetColor(color);
    ofCircle(pos.x, pos.y, particleRadius);
    ofPopStyle();
	
}


//------------------------------------------------------------
void particle::bounceOffWalls(){
	
	// sometimes it makes sense to damped, when we hit
	bool bDampedOnCollision = true;
	bool bDidICollide = false;
	
	// what are the walls
	float minx = 0;
	float miny = 0;
	float maxx = ofGetWidth();
	float maxy = ofGetHeight();
	
	if (pos.x > maxx){
		pos.x = maxx; // move to the edge, (important!)
		vel.x *= -1;
		bDidICollide = true;
	} else if (pos.x < minx){
		pos.x = minx; // move to the edge, (important!)
		vel.x *= -1;
		bDidICollide = true;
	}
	
	if (pos.y > maxy){
		pos.y = maxy; // move to the edge, (important!)
		vel.y *= -1;
		bDidICollide = true;
	} else if (pos.y < miny){
		pos.y = miny; // move to the edge, (important!)
		vel.y *= -1;
		bDidICollide = true;
	}
	
	if (bDidICollide == true && bDampedOnCollision == true){
		vel *= 0.3;
	}
	
}

//------------------------------------------------------------

void particle::changeColor(ofVec2f _pos, float _radius){
    
    ofVec2f posOfForce;
    posOfForce.set(_pos);
    
    ofVec2f diff	= pos - posOfForce;
	float length	= diff.length();
    
    bool bAmCloseEnough = true;
    if (_radius > 0){
        if (length > _radius){
            bAmCloseEnough = false;
        }
    }
    
    if (bAmCloseEnough == true){
		float pct = 1 - (length / _radius);  // stronger on the inside
        //		diff.normalize();
        color.setHsb(78, 100 + 155*pct, 240 + 10*pct, 255);
        particleRadius = 1.5 + 2 * pct;
    }
}